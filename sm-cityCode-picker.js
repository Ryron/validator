+ function($) {
    "use strict";
    var format = function(data,type) {
        var result = [];
        for(var i=0;i<data.length;i++) {
            var d = data[i];
            if(d.name === "请选择") continue;
            result.push(d[type]);
        }
        if(result.length) return result;
        return [""];
    };

    var sub = function(data,type) {
        if(!data.sub) return [""];
        return format(data.sub,type);
    };
    var getPorvinces = function(d,type){
        for(var i=0;i< raw.length;i++) {
            if(raw[i].name === d) return raw[i][type];
        }
        return [""];
    };
    var getCities = function(d,type) {
        for(var i=0;i< raw.length;i++) {
            if(raw[i].value === d) return sub(raw[i],type);
        }
        return [""];
    };

    var getDistricts = function(p, c,type) {
        for(var i=0;i< raw.length;i++) {
            if(raw[i].value === p) {
                for(var j=0;j< raw[i].sub.length;j++) {
                    if(raw[i].sub[j].value === c) {
                        return sub(raw[i].sub[j],type);
                    }
                }
            }
        }
        return [""];
    };

    var raw = $.smConfig.rawCitiesData;
    var provinces = raw.map(function(d) {
        return d.value;
    });
    var provincesDisplayValue = raw.map(function(d){
        return d.name;
    });
    var initCities = sub(raw[0],'value');
    var initDistricts = [""];



    var currentProvince = provinces[0];

    var currentCity = initCities[0];
    var currentDistrict = initDistricts[0];

    var initCitiesDisplayValue = sub(raw[0],'name');
    var initDistrictsDisplayValue = [];

    var t;
    var defaults = {

        cssClass: "city-picker",
        rotateEffect: false,  //为了性能

        onChange: function (picker, values, displayValues) {
            var newProvince = picker.cols[0].value;
            var newProvinceDisplay = picker.cols[0].displayValue;
            console.log("省="+newProvinceDisplay+","+newProvince);
            var newCity;
            if(newProvince !== currentProvince) {
                // 如果Province变化，节流以提高reRender性能
                clearTimeout(t);

                t = setTimeout(function(){
                    var newCities = getCities(newProvince,'value');
                    var newCitiesDisplayValues = getCities(newProvince,'name');

                    newCity = newCities[0];
                    var newDistricts = getDistricts(newProvince, newCity,'value');
                    var newDistrictsDisplayValues = getDistricts(newProvince, newCity,'name');
                    
                    picker.cols[1].replaceValues(newCities,newCitiesDisplayValues);
                    picker.cols[2].replaceValues(newDistricts,newDistrictsDisplayValues);
                    currentProvince = newProvince;
                    currentCity = newCity;
                    picker.updateValue();
                }, 200);
                return;
            }
            newCity = picker.cols[1].value;
            if(newCity !== currentCity) {
                picker.cols[2].replaceValues(getDistricts(newProvince, newCity,'value'),getDistricts(newProvince, newCity,'name'));
                currentCity = newCity;
                picker.updateValue();
            }
        },

        cols: [
        {
            textAlign: 'center',
            values: provinces,
            displayValues:provincesDisplayValue,
            cssClass: "col-province"
        },
        {
            textAlign: 'center',
            values: initCities,
            displayValues: initCitiesDisplayValue,
            cssClass: "col-city"
        },
        {
            textAlign: 'center',
            values: initDistricts,
            cssClass: "col-district"
        }
        ]
    };

    $.fn.cityPicker = function(params) {
        return this.each(function() {
            if(!this) return;
            var p = $.extend(defaults, params);
            //计算value
            if (p.value) {
                $(this).val(p.value.join(' '));
            } else {
                var val = $(this).val();
                val && (p.value = val.split(' '));
            }

            if (p.value) {
                //p.value = val.split(" ");
                if(p.value[0]) {
                    currentProvince = p.value[0];
                    p.cols[1].values = getCities(p.value[0]);
                }
                if(p.value[1]) {
                    currentCity = p.value[1];
                    p.cols[2].values = getDistricts(p.value[0], p.value[1]);
                } else {
                    p.cols[2].values = getDistricts(p.value[0], p.cols[1].values[0]);
                }
                !p.value[2] && (p.value[2] = '');
                currentDistrict = p.value[2];
            }
            $(this).picker(p);
        });
    };

}(Zepto);
